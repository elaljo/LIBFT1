#include "libft.h"

char	*ft_strdup(const char *str)
{
	size_t	l;
	char	*ptr;
	size_t	i;

	l = ft_strlen(str);
	ptr = (char *)malloc(l + 1 * sizeof(char));
	if (!ptr)
		return (NULL);
	i = 0;
	while (str[i] != '\0')
	{
		ptr[i] = str[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
